package DOMparser;

public class SokoBandMap {
		private int hard;
		private int level;
		private String name;
		private int [][] map;
		
		public SokoBandMap() {

		}

		public int getHard() {
			return hard;
		}

		public void setHard(int hard) {
			this.hard = hard;
		}

		public int getLevel() {
			return level;
		}

		public void setLevel(int level) {
			this.level = level;
		}

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}

		public int[][] getMap() {
			return map;
		}

		public void setMap(int[][] map) {
			this.map = map;
		}
		
		
		
}
